#!/bin/bash
#
# pyIRCX Installation Script
#
# Supports: Debian/Ubuntu, RHEL/CentOS/Fedora, Arch Linux, openSUSE
#
set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Default installation paths
INSTALL_DIR="/opt/pyircx"
CONFIG_DIR="/etc/pyircx"
SERVICE_USER="pyircx"
SERVICE_GROUP="pyircx"
INSTALL_CONF="/etc/pyircx/install.conf"

# Web installation paths (will be set by prompts or config)
WEB_ADMIN_DIR=""
WEBCHAT_WEB_DIR=""

# Detect OS
detect_os() {
    if [ -f /etc/os-release ]; then
        . /etc/os-release
        OS=$ID
        VERSION=$VERSION_ID
    elif [ -f /etc/redhat-release ]; then
        OS="rhel"
    elif [ -f /etc/debian_version ]; then
        OS="debian"
    else
        OS="unknown"
    fi
    echo "$OS"
}

# Check if running as root
check_root() {
    if [ "$EUID" -ne 0 ]; then
        echo -e "${RED}Error: This script must be run as root${NC}"
        echo "Try: sudo $0"
        exit 1
    fi
}

# Save installation configuration
save_install_config() {
    echo -e "${YELLOW}Saving installation configuration...${NC}"

    mkdir -p "$(dirname "$INSTALL_CONF")"

    cat > "$INSTALL_CONF" << EOF
# pyIRCX Installation Configuration
# Generated by install.sh on $(date)
# Used by upgrade.sh, repair.sh, and uninstall.sh

# Installation metadata
INSTALL_DATE="$(date +%Y-%m-%d)"
INSTALL_VERSION="2.0.0"
INSTALL_USER="$USER"

# Core installation paths
PYIRCX_DIR="$INSTALL_DIR"
CONFIG_DIR="$CONFIG_DIR"
CONFIG_FILE="$CONFIG_DIR/pyircx_config.json"

# Web components
WEBADMIN_DIR="$WEB_ADMIN_DIR"
WEBADMIN_ENABLED="$([ -n "$WEB_ADMIN_DIR" ] && echo "true" || echo "false")"

WEBCHAT_DIR="$WEBCHAT_WEB_DIR"
WEBCHAT_ENABLED="$([ -n "$WEBCHAT_WEB_DIR" ] && echo "true" || echo "false")"
WEBCHAT_GATEWAY_DIR="$INSTALL_DIR/webchat"

# Web server
WEB_USER="${WEB_USER:-apache}"

# Database
DATABASE_PATH="$INSTALL_DIR/pyircx.db"

# Service
SERVICE_ENABLED="true"
SERVICE_USER="$SERVICE_USER"
SERVICE_GROUP="$SERVICE_GROUP"
EOF

    chmod 644 "$INSTALL_CONF"
    echo -e "${GREEN}Installation configuration saved to $INSTALL_CONF${NC}"
}

# Load installation configuration
load_install_config() {
    if [ -f "$INSTALL_CONF" ]; then
        echo -e "${YELLOW}Loading existing installation configuration...${NC}"
        source "$INSTALL_CONF"

        # Set variables from config
        WEB_ADMIN_DIR="${WEBADMIN_DIR:-}"
        WEBCHAT_WEB_DIR="${WEBCHAT_DIR:-}"

        return 0
    fi
    return 1
}

# Prompt for WebAdmin installation path
prompt_webadmin_path() {
    echo ""
    echo "========================================"
    echo "Web Administration Panel - Installation Path"
    echo "========================================"
    echo ""
    echo "Where should the Web Admin Panel be installed?"
    echo ""
    echo "Options:"
    echo "  1. /var/www/html/webadmin (recommended, subdirectory)"
    echo "  2. /var/www/html (direct in webroot)"
    echo "  3. Custom path"
    echo "  4. Skip installation"
    echo ""
    read -p "Enter choice [1-4] (default: 1): " WEBADMIN_CHOICE

    case "${WEBADMIN_CHOICE:-1}" in
        1)
            WEB_ADMIN_DIR="/var/www/html/webadmin"
            echo -e "${GREEN}WebAdmin will be installed to: $WEB_ADMIN_DIR${NC}"
            ;;
        2)
            WEB_ADMIN_DIR="/var/www/html"
            echo -e "${YELLOW}Warning: Installing directly in webroot${NC}"
            echo -e "${GREEN}WebAdmin will be installed to: $WEB_ADMIN_DIR${NC}"
            ;;
        3)
            read -p "Enter custom path: " CUSTOM_PATH
            WEB_ADMIN_DIR="${CUSTOM_PATH:-/var/www/html/webadmin}"
            echo -e "${GREEN}WebAdmin will be installed to: $WEB_ADMIN_DIR${NC}"
            ;;
        4)
            WEB_ADMIN_DIR=""
            echo -e "${YELLOW}WebAdmin installation skipped${NC}"
            ;;
        *)
            WEB_ADMIN_DIR="/var/www/html/webadmin"
            echo -e "${GREEN}WebAdmin will be installed to: $WEB_ADMIN_DIR (default)${NC}"
            ;;
    esac
}

