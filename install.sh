# Copy files
copy_files() {
    echo -e "${YELLOW}Copying files...${NC}"

    # Get the directory where this script is located
    SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

    # Copy main script and modules
    cp "$SCRIPT_DIR/pyircx.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/channel.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/config.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/database.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/help_text.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/modes.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/responses.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/security.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/service_bot.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/ssl_manager.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/user.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/validation.py" "$INSTALL_DIR/"
    cp "$SCRIPT_DIR/api.py" "$INSTALL_DIR/" 2>/dev/null || true
    cp "$SCRIPT_DIR/api_helpers.py" "$INSTALL_DIR/" 2>/dev/null || true
    cp "$SCRIPT_DIR/db_pool.py" "$INSTALL_DIR/" 2>/dev/null || true
    cp "$SCRIPT_DIR/linking.py" "$INSTALL_DIR/"

    # Copy or create config
    created_config="false"
    if [ ! -f "$CONFIG_DIR/pyircx_config.json" ]; then
        if [ -f "$SCRIPT_DIR/pyircx_config.json" ]; then
            # Use provided config template
            cp "$SCRIPT_DIR/pyircx_config.json" "$CONFIG_DIR/"
            echo "Copied config template"
            created_config="true"
        else
            # Generate default config from pyircx.py
            echo "Generating default config..."
            python3 "$SCRIPT_DIR/generate_default_config.py" "$INSTALL_DIR/pyircx.py" "$CONFIG_DIR/pyircx_config.json"
            created_config="true"
        fi
    else
        echo "Config already exists, not overwriting"
    fi

    if [ "$created_config" = "true" ]; then
        python3 - "$CONFIG_DIR/pyircx_config.json" "$GENERATED_ADMIN_PASS" "$GENERATED_WEBIRC_PASS" <<'PY'
import json
import sys

cfg_path, admin_pass, webirc_pass = sys.argv[1], sys.argv[2], sys.argv[3]

with open(cfg_path, 'r', encoding='utf-8') as f:
    cfg = json.load(f)

cfg.setdefault('admin', {})
cfg['admin']['default_password'] = admin_pass

cfg.setdefault('security', {})
cfg['security']['auth_require_ssl'] = True
cfg['security']['pass_require_ssl'] = True

cfg['security'].setdefault('webirc', {})
cfg['security']['webirc'].setdefault('hosts', {})
if not cfg['security']['webirc']['hosts']:
    cfg['security']['webirc']['hosts']['pyircx-webchat'] = {'allowed_ips': ['127.0.0.1', '::1']}
for host_cfg in cfg['security']['webirc']['hosts'].values():
    host_cfg['password'] = webirc_pass

cfg.setdefault('linking', {})
cfg['linking'].setdefault('tls', {})
cfg['linking']['tls']['enabled'] = True
cfg['linking']['tls']['required'] = True
cfg['linking']['tls']['verify'] = cfg['linking']['tls'].get('verify', True)

with open(cfg_path, 'w', encoding='utf-8') as f:
    json.dump(cfg, f, indent=2)
PY
    fi

    # Create symlink for config
    ln -sf "$CONFIG_DIR/pyircx_config.json" "$INSTALL_DIR/pyircx_config.json"

    echo -e "${GREEN}Files copied${NC}"
}

# Set permissions
set_permissions() {
    echo -e "${YELLOW}Setting permissions...${NC}"

    chown -R "$SERVICE_USER:$SERVICE_GROUP" "$INSTALL_DIR"
    chown -R "$SERVICE_USER:$SERVICE_GROUP" "$CONFIG_DIR"
    chown root:"$SERVICE_GROUP" "$CONFIG_DIR/pyircx_config.json" 2>/dev/null || true
    chmod 775 "$INSTALL_DIR"  # Group needs write for SQLite journal files
    chmod 775 "$CONFIG_DIR"  # Group needs write for web admin config edits
    chmod 750 "$INSTALL_DIR/transcripts"  # Keep transcripts private
    chmod 660 "$INSTALL_DIR/pyircx.db" 2>/dev/null || true  # Database group writable
    chmod 660 "$CONFIG_DIR/pyircx_config.json" 2>/dev/null || true  # Config group-writable (needed for webadmin)
    touch "$INSTALL_DIR/admin_commands.queue" 2>/dev/null || true  # Create admin command queue
    chmod 660 "$INSTALL_DIR/admin_commands.queue" 2>/dev/null || true  # Queue group-writable (needed for webadmin)
    chmod 755 "$INSTALL_DIR/pyircx.py"
    chmod 755 "$INSTALL_DIR/api.py" 2>/dev/null || true
    chmod 755 "$INSTALL_DIR/linking.py" 2>/dev/null || true

    # Add web server user to pyircx group for database access
    # Detect web server user (apache, www-data, or http)
    WEB_USER="$(detect_web_user)"

    if [ -n "$WEB_USER" ]; then
        echo -e "${YELLOW}Adding $WEB_USER to $SERVICE_GROUP group for database access...${NC}"
        usermod -a -G "$SERVICE_GROUP" "$WEB_USER"
        echo -e "${GREEN}✓ Web server user added to group${NC}"

        # Restart PHP-FPM to apply group membership
        if systemctl is-active --quiet php-fpm 2>/dev/null; then
            echo -e "${YELLOW}Restarting PHP-FPM to apply group membership...${NC}"
            systemctl restart php-fpm
            echo -e "${GREEN}✓ PHP-FPM restarted${NC}"
        fi
    fi

    echo -e "${GREEN}Permissions set${NC}"
}

# Install Web Admin Panel
install_web_admin() {
    echo -e "${YELLOW}Installing Web Administration Panel...${NC}"

    # Check if path is set
    if [ -z "$WEB_ADMIN_DIR" ]; then
        echo -e "${RED}Error: WEB_ADMIN_DIR not set${NC}"
        return 1
    fi

    local os=$(detect_os)
    SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

    # Install Apache and PHP based on OS
    case "$os" in
        fedora|rhel|centos|rocky|almalinux|oracle|scientific)
            echo -e "${YELLOW}Installing Apache and PHP...${NC}"
            dnf install -y httpd php php-fpm php-json php-mbstring || yum install -y httpd php php-fpm php-json php-mbstring
            systemctl enable --now httpd
            systemctl enable --now php-fpm
            ;;
        debian|ubuntu|linuxmint|pop|elementary|zorin|kali|parrot|raspbian)
            echo -e "${YELLOW}Installing Apache and PHP...${NC}"
            apt-get update
            apt-get install -y apache2 php php-fpm php-json php-mbstring
            a2enmod rewrite
            systemctl enable --now apache2
            ;;
        arch|manjaro|endeavouros|garuda)
            echo -e "${YELLOW}Installing Apache and PHP...${NC}"
            pacman -S --noconfirm apache php php-fpm
            systemctl enable --now httpd
            systemctl enable --now php-fpm
            ;;
        *)
            echo -e "${YELLOW}Please install Apache and PHP manually for your OS${NC}"
            return 1
            ;;
    esac

    # Create web admin directory
    mkdir -p "$WEB_ADMIN_DIR"

    # Copy web admin files
    if [ -d "$SCRIPT_DIR/webadmin" ]; then
        cp "$SCRIPT_DIR/webadmin"/*.php "$WEB_ADMIN_DIR/" 2>/dev/null || true
        cp "$SCRIPT_DIR/webadmin"/*.js "$WEB_ADMIN_DIR/" 2>/dev/null || true
        cp "$SCRIPT_DIR/webadmin"/*.css "$WEB_ADMIN_DIR/" 2>/dev/null || true
        cp "$SCRIPT_DIR/webadmin"/.htaccess "$WEB_ADMIN_DIR/" 2>/dev/null || true
    else
        echo -e "${RED}Web admin files not found${NC}"
        return 1
    fi

    WEB_USER="$(detect_web_user)"
    if [ -n "$WEB_USER" ]; then
        chown -R "$WEB_USER:$WEB_USER" "$WEB_ADMIN_DIR"
    fi
    chmod 755 "$WEB_ADMIN_DIR"
    chmod 644 "$WEB_ADMIN_DIR"/*.php 2>/dev/null || true
    chmod 644 "$WEB_ADMIN_DIR"/*.js 2>/dev/null || true
    chmod 644 "$WEB_ADMIN_DIR"/*.css 2>/dev/null || true
    chmod 644 "$WEB_ADMIN_DIR"/.htaccess 2>/dev/null || true

    # Add web user to systemd-journal group for log access
    if [ -n "$WEB_USER" ]; then
        usermod -a -G systemd-journal "$WEB_USER" 2>/dev/null || true
    fi

    # Configure SELinux if enabled
    if command -v getenforce &>/dev/null && [ "$(getenforce)" != "Disabled" ]; then
        echo -e "${YELLOW}SELinux detected, installing policies...${NC}"
        install_selinux
    fi

    # Install polkit rules
    install_polkit

    echo -e "${GREEN}Web Admin Panel installed successfully!${NC}"
    echo ""
    echo "Access at: http://localhost/webadmin/"
    echo "Login with your IRC staff account (administrators only)"
    echo ""
}

# Install SELinux policies
install_selinux() {
    SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

    if [ ! -d "$SCRIPT_DIR/selinux" ]; then
        echo -e "${YELLOW}SELinux policy files not found, skipping${NC}"
        return 0
    fi

    echo -e "${YELLOW}Installing SELinux policies...${NC}"

    cd "$SCRIPT_DIR/selinux"

    # Compile and install httpd-systemd policy
    if [ -f pyircx-httpd-systemd.te ]; then
        checkmodule -M -m -o pyircx-httpd-systemd.mod pyircx-httpd-systemd.te
        semodule_package -o pyircx-httpd-systemd.pp -m pyircx-httpd-systemd.mod
        semodule -i pyircx-httpd-systemd.pp
        echo -e "${GREEN}✓ httpd-systemd policy installed${NC}"
    fi

    # Compile and install httpd-journal policy
    if [ -f pyircx-httpd-journal-v3.te ]; then
        checkmodule -M -m -o pyircx-httpd-journal-v3.mod pyircx-httpd-journal-v3.te
        semodule_package -o pyircx-httpd-journal-v3.pp -m pyircx-httpd-journal-v3.mod
        semodule -i pyircx-httpd-journal-v3.pp
        echo -e "${GREEN}✓ httpd-journal policy installed${NC}"
    fi

    # Install httpd-reload policy (allows webadmin to reload pyircx service)
    if [ -f pyircx-httpd-reload.pp ]; then
        semodule -i pyircx-httpd-reload.pp
        echo -e "${GREEN}✓ httpd-reload policy installed${NC}"
    fi

    cd - > /dev/null

    # Configure SELinux file contexts (comprehensive configuration)
    if command -v semanage &> /dev/null && command -v restorecon &> /dev/null; then
        echo -e "${YELLOW}Configuring SELinux file contexts...${NC}"

        # /opt/pyircx - Main installation (read-write for web admin API)
        semanage fcontext -d -t httpd_sys_rw_content_t "/opt/pyircx(/.*)?" 2>/dev/null || true
        semanage fcontext -a -t httpd_sys_rw_content_t "/opt/pyircx(/.*)?" 2>/dev/null || true

        # /etc/pyircx - Configuration directory (read-write for web admin config editor)
        #               EXCEPT webchat.conf which needs etc_t for systemd EnvironmentFile
        semanage fcontext -d -t httpd_sys_rw_content_t "/etc/pyircx(/.*)?" 2>/dev/null || true
        semanage fcontext -a -t httpd_sys_rw_content_t "/etc/pyircx(/.*)?" 2>/dev/null || true

        # /etc/pyircx/webchat.conf - SystemD EnvironmentFile (MUST be etc_t, NOT httpd_sys_rw_content_t)
        # This overrides the previous rule specifically for webchat.conf
        semanage fcontext -d -t etc_t "/etc/pyircx/webchat\.conf" 2>/dev/null || true
        semanage fcontext -a -t etc_t "/etc/pyircx/webchat\.conf" 2>/dev/null || true

        # WebAdmin - only if directory exists or will be created
        if [ -n "$WEB_ADMIN_DIR" ] || [ -d "/var/www/html/webadmin" ]; then
            ADMIN_PATH="${WEB_ADMIN_DIR:-/var/www/html/webadmin}"
            semanage fcontext -d -t httpd_sys_rw_content_t "$ADMIN_PATH(/.*)?" 2>/dev/null || true
            semanage fcontext -a -t httpd_sys_rw_content_t "$ADMIN_PATH(/.*)?" 2>/dev/null || true
        fi

        # WebChat - only if directory exists or will be created
        if [ -n "$WEBCHAT_WEB_DIR" ] || [ -d "/var/www/html/webchat" ]; then
            CHAT_PATH="${WEBCHAT_WEB_DIR:-/var/www/html/webchat}"
            semanage fcontext -d -t httpd_sys_content_t "$CHAT_PATH(/.*)?" 2>/dev/null || true
            semanage fcontext -a -t httpd_sys_content_t "$CHAT_PATH(/.*)?" 2>/dev/null || true
        fi

        # Apply all contexts
        restorecon -Rv /opt/pyircx 2>/dev/null || true
        restorecon -Rv /etc/pyircx 2>/dev/null || true
        [ -n "$WEB_ADMIN_DIR" ] && [ -d "$WEB_ADMIN_DIR" ] && restorecon -Rv "$WEB_ADMIN_DIR" 2>/dev/null || true
        [ -n "$WEBCHAT_WEB_DIR" ] && [ -d "$WEBCHAT_WEB_DIR" ] && restorecon -Rv "$WEBCHAT_WEB_DIR" 2>/dev/null || true

        # CRITICAL: Explicitly fix webchat.conf after global restorecon
        if [ -f "/etc/pyircx/webchat.conf" ]; then
            chcon -t etc_t "/etc/pyircx/webchat.conf" 2>/dev/null || true
            echo -e "${GREEN}✓ SELinux context fixed for webchat.conf (etc_t)${NC}"
        fi

        echo -e "${GREEN}✓ SELinux file contexts configured${NC}"
    fi

    echo -e "${GREEN}SELinux policies installed${NC}"
}

# Install Unbound DNS resolver
install_unbound() {
    echo -e "${YELLOW}Installing Unbound DNS resolver...${NC}"

    local os=$(detect_os)

    # Install unbound based on OS
    case "$os" in
        ubuntu|debian|linuxmint|pop|elementary|zorin|kali|parrot|raspbian)
            apt-get update
            apt-get install -y unbound
            ;;
        fedora)
            dnf install -y unbound
            ;;
        centos|rhel|rocky|almalinux|oracle|scientific)
            if [ -f /etc/centos-release ] || [ "$VERSION_ID" -lt 8 ]; then
                yum install -y unbound
            else
                dnf install -y unbound
            fi
            ;;
        arch|manjaro|endeavouros|garuda|artix)
            pacman -Sy --noconfirm unbound
            ;;
        opensuse*|sles|tumbleweed)
            zypper install -y unbound
            ;;
        gentoo|funtoo)
            emerge --ask=n net-dns/unbound
            ;;
        void)
            xbps-install -Sy unbound
            ;;
        alpine)
            apk add --no-cache unbound
            ;;
        *)
            echo -e "${YELLOW}Unknown OS ($os), attempting generic installation...${NC}"
            if command -v apt-get &> /dev/null; then
                apt-get update && apt-get install -y unbound
            elif command -v dnf &> /dev/null; then
                dnf install -y unbound
            elif command -v yum &> /dev/null; then
                yum install -y unbound
            elif command -v pacman &> /dev/null; then
                pacman -Sy --noconfirm unbound
            elif command -v zypper &> /dev/null; then
                zypper install -y unbound
            elif command -v apk &> /dev/null; then
                apk add --no-cache unbound
            else
                echo -e "${RED}No supported package manager found for unbound installation${NC}"
                return 1
            fi
            ;;
    esac

    # Create unbound configuration for local caching resolver
    echo -e "${YELLOW}Configuring unbound as local resolver...${NC}"

    # Create unbound.conf.d directory if it doesn't exist
    mkdir -p /etc/unbound/unbound.conf.d

    # Backup existing config if present
    if [ -f /etc/unbound/unbound.conf ]; then
        cp /etc/unbound/unbound.conf /etc/unbound/unbound.conf.backup.$(date +%Y%m%d%H%M%S)
    fi

    # Create pyircx-specific unbound configuration
    cat > /etc/unbound/unbound.conf.d/pyircx.conf << 'EOF'
# pyIRCX Unbound Configuration
# Local caching DNS resolver for IRC hostname resolution

server:
    # Listen on localhost (127.0.0.53 to avoid conflicts with systemd-resolved)
    interface: 127.0.0.53
    port: 53

    # Access control - only allow localhost
    access-control: 127.0.0.0/8 allow
    access-control: ::1/128 allow

    # Performance settings
    num-threads: 2
    msg-cache-size: 16m
    rrset-cache-size: 32m
    cache-min-ttl: 300
    cache-max-ttl: 86400

    # Privacy settings - don't send client info upstream
    hide-identity: yes
    hide-version: yes
    qname-minimisation: yes

    # Security settings
    harden-glue: yes
    harden-dnssec-stripped: yes
    harden-referral-path: yes
    use-caps-for-id: yes

    # Logging (minimal for production)
    verbosity: 1
    log-queries: no

    # Root hints for recursive resolution
    root-hints: "/var/lib/unbound/root.hints"

    # Enable prefetching for better performance
    prefetch: yes
    prefetch-key: yes
EOF

    # Ensure main config includes the conf.d directory
    if ! grep -q "include.*unbound.conf.d" /etc/unbound/unbound.conf 2>/dev/null; then
        echo 'include: "/etc/unbound/unbound.conf.d/*.conf"' >> /etc/unbound/unbound.conf
    fi

    # Download root hints if not present
    if [ ! -f /var/lib/unbound/root.hints ]; then
        echo -e "${YELLOW}Downloading root hints...${NC}"
        mkdir -p /var/lib/unbound
        if command -v wget &> /dev/null; then
            wget -q -O /var/lib/unbound/root.hints https://www.internic.net/domain/named.root
        elif command -v curl &> /dev/null; then
            curl -s -o /var/lib/unbound/root.hints https://www.internic.net/domain/named.root
        else
            echo -e "${YELLOW}Could not download root hints (no wget/curl). Using system defaults.${NC}"
        fi
        chown unbound:unbound /var/lib/unbound/root.hints 2>/dev/null || true
    fi

    # Set permissions
    chown -R unbound:unbound /etc/unbound/unbound.conf.d 2>/dev/null || true
    chmod 644 /etc/unbound/unbound.conf.d/pyircx.conf

    # Check configuration
    echo -e "${YELLOW}Checking unbound configuration...${NC}"
    if unbound-checkconf 2>/dev/null; then
        echo -e "${GREEN}✓ Unbound configuration is valid${NC}"
    else
        echo -e "${RED}⚠ Unbound configuration check failed, using defaults${NC}"
        rm -f /etc/unbound/unbound.conf.d/pyircx.conf
    fi

    # Enable and start unbound
    echo -e "${YELLOW}Enabling unbound service...${NC}"
    systemctl enable unbound
    systemctl restart unbound

    if systemctl is-active --quiet unbound; then
        echo -e "${GREEN}✓ Unbound is running${NC}"
    else
        echo -e "${RED}⚠ Unbound failed to start. Check: journalctl -u unbound${NC}"
        return 1
    fi

    # Configure system to use unbound for DNS
    echo -e "${YELLOW}Configuring system to use unbound for DNS resolution...${NC}"

    # Detect init system and DNS management
    if command -v resolvectl &> /dev/null && systemctl is-active --quiet systemd-resolved; then
        # systemd-resolved is active - configure it to use unbound
        echo -e "${YELLOW}Configuring systemd-resolved to use unbound...${NC}"

        mkdir -p /etc/systemd/resolved.conf.d
        cat > /etc/systemd/resolved.conf.d/unbound.conf << 'EOF'
[Resolve]
DNS=127.0.0.1
DNSStubListener=no
EOF
        systemctl restart systemd-resolved

        # Write a static resolv.conf pointing only to unbound.
        # Interface-level DHCP DNS servers would otherwise take precedence in the
        # uplink resolv.conf and bypass unbound entirely (including Spamhaus XBL,
        # which returns 127.255.255.254 "policy" responses for public resolvers).
        rm -f /etc/resolv.conf
        printf 'nameserver 127.0.0.1\n' > /etc/resolv.conf
        chattr +i /etc/resolv.conf 2>/dev/null || true

        echo -e "${GREEN}✓ systemd-resolved configured to use unbound${NC}"

    elif [ -f /etc/NetworkManager/NetworkManager.conf ] && systemctl is-active --quiet NetworkManager; then
        # NetworkManager is active - configure it to use unbound
        echo -e "${YELLOW}Configuring NetworkManager to use unbound...${NC}"

        mkdir -p /etc/NetworkManager/conf.d
        cat > /etc/NetworkManager/conf.d/unbound.conf << 'EOF'
[main]
dns=none
EOF
        # Set resolv.conf to use unbound
        cat > /etc/resolv.conf << 'EOF'
# Generated by pyIRCX install.sh for unbound
nameserver 127.0.0.53
EOF
        chattr +i /etc/resolv.conf 2>/dev/null || true
        systemctl restart NetworkManager

        echo -e "${GREEN}✓ NetworkManager configured to use unbound${NC}"

    else
        # Direct resolv.conf modification
        echo -e "${YELLOW}Configuring /etc/resolv.conf to use unbound...${NC}"

        # Backup existing resolv.conf
        if [ -f /etc/resolv.conf ] && [ ! -L /etc/resolv.conf ]; then
            cp /etc/resolv.conf /etc/resolv.conf.backup.$(date +%Y%m%d%H%M%S)
        fi

        # Check if resolv.conf is immutable
        if lsattr /etc/resolv.conf 2>/dev/null | grep -q 'i'; then
            chattr -i /etc/resolv.conf 2>/dev/null || true
        fi

        cat > /etc/resolv.conf << 'EOF'
# Generated by pyIRCX install.sh for unbound
nameserver 127.0.0.53
EOF
        chattr +i /etc/resolv.conf 2>/dev/null || true

        echo -e "${GREEN}✓ /etc/resolv.conf configured to use unbound${NC}"
    fi

    # Test DNS resolution
    echo -e "${YELLOW}Testing DNS resolution...${NC}"
    if host google.com 127.0.0.53 &>/dev/null || dig @127.0.0.53 google.com +short &>/dev/null; then
        echo -e "${GREEN}✓ DNS resolution via unbound is working${NC}"
    else
        echo -e "${YELLOW}⚠ DNS test failed - unbound may need additional configuration${NC}"
        echo "  Check: journalctl -u unbound"
        echo "  Test: dig @127.0.0.53 google.com"
    fi

    echo -e "${GREEN}Unbound DNS resolver installed and configured!${NC}"
    echo ""
    echo "Unbound configuration: /etc/unbound/unbound.conf.d/pyircx.conf"
    echo "Service commands:"
    echo "  systemctl status unbound   - Check status"
    echo "  systemctl restart unbound  - Restart resolver"
    echo ""
}

