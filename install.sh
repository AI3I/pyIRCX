            echo -e "${YELLOW}WebAdmin installation skipped${NC}"
            ;;
        *)
            WEB_ADMIN_DIR="/var/www/html/webadmin"
            echo -e "${GREEN}WebAdmin will be installed to: $WEB_ADMIN_DIR (default)${NC}"
            ;;
    esac
}

