# Check version consistency across project files
check_version_consistency() {
    echo -e "${YELLOW}Checking version consistency...${NC}"

    SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
    VERSION_ISSUES=0

    # Get version from pyircx.py
    if [ -f "$SCRIPT_DIR/pyircx.py" ]; then
        PYIRCX_VERSION=$(grep "__version__" "$SCRIPT_DIR/pyircx.py" | head -1 | cut -d'"' -f2)
        echo -e "${GREEN}  pyircx.py version: $PYIRCX_VERSION${NC}"

        # Check against INSTALL_VERSION
        if [ "$PYIRCX_VERSION" != "$INSTALL_VERSION" ]; then
            echo -e "${RED}  ✗ Version mismatch: install.sh has $INSTALL_VERSION${NC}"
            VERSION_ISSUES=$((VERSION_ISSUES + 1))
        else
            echo -e "${GREEN}  ✓ install.sh version matches${NC}"
        fi

        # Check webadmin/index.php
        if [ -f "$SCRIPT_DIR/webadmin/index.php" ]; then
            WEBADMIN_VER=$(grep -o "pyIRCX v[0-9]\+\.[0-9]\+\.[0-9]\+" "$SCRIPT_DIR/webadmin/index.php" | head -1 | sed 's/pyIRCX v//')
            if [ "$PYIRCX_VERSION" != "$WEBADMIN_VER" ]; then
                echo -e "${RED}  ✗ Version mismatch: webadmin/index.php has $WEBADMIN_VER${NC}"
                VERSION_ISSUES=$((VERSION_ISSUES + 1))
            else
                echo -e "${GREEN}  ✓ webadmin/index.php version matches${NC}"
            fi
        fi

        # Check webchat/index.html
        if [ -f "$SCRIPT_DIR/webchat/index.html" ]; then
            WEBCHAT_VER=$(grep -o "v[0-9]\+\.[0-9]\+\.[0-9]\+</span>" "$SCRIPT_DIR/webchat/index.html" | head -1 | sed 's/v\(.*\)<\/span>/\1/')
            if [ "$PYIRCX_VERSION" != "$WEBCHAT_VER" ]; then
                echo -e "${RED}  ✗ Version mismatch: webchat/index.html has $WEBCHAT_VER${NC}"
                VERSION_ISSUES=$((VERSION_ISSUES + 1))
            else
                echo -e "${GREEN}  ✓ webchat/index.html version matches${NC}"
            fi
        fi

        if [ $VERSION_ISSUES -gt 0 ]; then
            echo -e "${YELLOW}  ⚠ Found $VERSION_ISSUES version inconsistency issue(s)${NC}"
            echo -e "${YELLOW}  This may indicate the installation files need to be updated${NC}"
            read -p "Continue anyway? (y/n) " -n 1 -r
            echo
            if [[ ! $REPLY =~ ^[Yy]$ ]]; then
                exit 1
            fi
        fi
    else
        echo -e "${YELLOW}  ⚠ pyircx.py not found, skipping version check${NC}"
    fi
}

# Check Python version
check_python() {
    echo -e "${YELLOW}Checking Python version...${NC}"

    if command -v python3 &> /dev/null; then
        PYTHON_VERSION=$(python3 -c 'import sys; print(f"{sys.version_info.major}.{sys.version_info.minor}")')
        PYTHON_MAJOR=$(echo $PYTHON_VERSION | cut -d. -f1)
        PYTHON_MINOR=$(echo $PYTHON_VERSION | cut -d. -f2)

        if [ "$PYTHON_MAJOR" -ge 3 ] && [ "$PYTHON_MINOR" -ge 8 ]; then
            echo -e "${GREEN}Python $PYTHON_VERSION found${NC}"
            return 0
        fi
    fi

    echo -e "${RED}Error: Python 3.8+ is required${NC}"
    echo "Please install Python 3.8 or later"
    exit 1
}

# Install dependencies based on OS
install_dependencies() {
    local os=$(detect_os)
    echo -e "${YELLOW}Installing dependencies for $os...${NC}"

    case $os in
        ubuntu|debian|linuxmint|pop|elementary|zorin|kali|parrot|raspbian)
            apt-get update
            apt-get install -y python3 python3-pip python3-venv
            ;;
        fedora)
            dnf install -y python3 python3-pip
            ;;
        centos|rhel|rocky|almalinux|oracle|scientific)
            if [ -f /etc/centos-release ] || [ "$VERSION_ID" -lt 8 ]; then
                yum install -y python3 python3-pip
            else
                dnf install -y python3 python3-pip
            fi
            ;;
        arch|manjaro|endeavouros|garuda|artix)
            pacman -Sy --noconfirm python python-pip
            ;;
        opensuse*|sles|tumbleweed)
            zypper install -y python3 python3-pip
            ;;
        gentoo|funtoo)
            emerge --ask=n dev-lang/python dev-python/pip
            ;;
        void)
            xbps-install -Sy python3 python3-pip
            ;;
        alpine)
            apk add --no-cache python3 py3-pip
            ;;
        solus)
            eopkg install -y python3 pip
            ;;
        nixos)
            echo -e "${YELLOW}NixOS detected. Please add python3 and pip to your configuration.nix${NC}"
            echo "Example: environment.systemPackages = with pkgs; [ python3 python3Packages.pip ];"
            ;;
        clear-linux-os)
            swupd bundle-add python3-basic
            ;;
        mageia)
            urpmi python3 python3-pip
            ;;
        slackware)
            # Slackware typically has Python pre-installed
            echo -e "${GREEN}Slackware detected. Python should be pre-installed.${NC}"
            if ! command -v pip3 &> /dev/null; then
                echo -e "${YELLOW}pip not found. Installing pip...${NC}"
                python3 -m ensurepip --default-pip || python3 <(curl -sS https://bootstrap.pypa.io/get-pip.py)
            fi
            ;;
        *)
            echo -e "${YELLOW}Unknown OS ($os), attempting generic installation...${NC}"
            # Try to detect package manager and install
            if command -v apt-get &> /dev/null; then
                apt-get update && apt-get install -y python3 python3-pip python3-venv
            elif command -v dnf &> /dev/null; then
                dnf install -y python3 python3-pip
            elif command -v yum &> /dev/null; then
                yum install -y python3 python3-pip
            elif command -v pacman &> /dev/null; then
                pacman -Sy --noconfirm python python-pip
            elif command -v zypper &> /dev/null; then
                zypper install -y python3 python3-pip
            elif command -v apk &> /dev/null; then
                apk add --no-cache python3 py3-pip
            elif command -v emerge &> /dev/null; then
                emerge --ask=n dev-lang/python dev-python/pip
            else
                echo -e "${YELLOW}No supported package manager found.${NC}"
                echo -e "${YELLOW}Please ensure Python 3.8+ and pip are installed manually.${NC}"
            fi
            ;;
    esac
}

