#!/bin/bash
#
# pyIRCX Installation Script
#
# Supports: Debian/Ubuntu, RHEL/CentOS/Fedora, Arch Linux, openSUSE
#
set -e

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
NC='\033[0m' # No Color

# Version
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
INSTALL_VERSION="$(python3 -c 'import json, pathlib; print(json.load(open(pathlib.Path("'"$SCRIPT_DIR"'") / "version.json"))["version"])')"

# Default installation paths
INSTALL_DIR="/opt/pyircx"
CONFIG_DIR="/etc/pyircx"
SERVICE_USER="pyircx"
SERVICE_GROUP="pyircx"
INSTALL_CONF="/etc/pyircx/install.conf"

# Web installation paths (will be set by prompts or config)
WEB_ADMIN_DIR=""
WEBCHAT_WEB_DIR=""

# DNS resolver (will be set by prompts)
UNBOUND_INSTALLED="false"

# Generated secrets for first-time install
GENERATED_ADMIN_PASS="__CHANGE_ME__"
GENERATED_WEBIRC_PASS="__CHANGE_ME__"
NONINTERACTIVE="${PYIRCX_NONINTERACTIVE:-0}"
