    echo -e "${YELLOW}Optional: Web Server Setup (Apache/httpd)${NC}"
    echo "========================================"
    echo ""
    echo "Setting up Apache/httpd is recommended before SSL configuration."
    echo "This configures Apache for WebChat, WebAdmin, and SSL certificates."
    echo ""
    echo "The setup script will:"
    echo "  - Install Apache/httpd and PHP (if needed)"
    echo "  - Configure virtual hosts for WebChat and WebAdmin"
    echo "  - Set up proper permissions and SELinux contexts"
    echo "  - Enable required Apache modules"
    echo ""
    if prompt_yes_no "Set up Apache/httpd now?" "yes" "$INSTALL_APACHE_PROMPT"; then
        if [ -f "setup_apache.sh" ]; then
            echo ""
            echo "Running Apache setup..."
            ./setup_apache.sh
        else
            echo -e "${YELLOW}setup_apache.sh not found in current directory${NC}"
            echo "You can run it later: sudo ./setup_apache.sh"
        fi
    else
        echo ""
        echo "Apache setup skipped. You can run it later with:"
        echo "  sudo ./setup_apache.sh"
    fi
    echo ""
    echo "========================================"
    echo -e "${YELLOW}Optional: SSL/TLS Certificate Setup${NC}"
    echo "========================================"
    echo ""
    echo "Would you like to set up SSL/TLS now?"
    echo "  - Let's Encrypt (free, automatic)"
    echo "  - Self-signed (for testing)"
    echo "  - Skip (configure manually later)"
    echo ""
    if prompt_yes_no "Set up SSL/TLS now?" "no" "$INSTALL_SSL_PROMPT"; then
        if [ -f "setup_ssl.sh" ]; then
            ./setup_ssl.sh "$CONFIG_DIR/pyircx_config.json"
        else
            echo -e "${YELLOW}setup_ssl.sh not found, skipping SSL setup${NC}"
            echo "You can run it later: sudo ./setup_ssl.sh"
        fi
    else
        echo ""
        echo "SSL/TLS can be set up later with:"
        echo "  sudo ./setup_ssl.sh"
    fi
    echo ""
    echo "========================================"
    echo -e "${YELLOW}Optional: Web Administration Panel${NC}"
    echo "========================================"
    echo ""
    echo "The Web Admin Panel provides a browser-based interface for"
    echo "managing the IRC server. Requires Apache and PHP."
    echo ""
    if prompt_yes_no "Install Web Administration Panel?" "no" "$INSTALL_WEBADMIN_PROMPT"; then
        prompt_webadmin_path
        if [ -n "$WEB_ADMIN_DIR" ]; then
            install_web_admin
        fi
    fi

    echo ""
    echo "========================================"
    echo -e "${YELLOW}Optional: WebChat (Browser IRC Client)${NC}"
    echo "========================================"
    echo ""
    echo "WebChat provides a browser-based IRC client that connects"
    echo "via WebSocket. Requires a web server for HTTPS."
    echo ""
    if prompt_yes_no "Install WebChat browser client?" "no" "$INSTALL_WEBCHAT_PROMPT"; then
        prompt_webchat_path
        if [ -n "$WEBCHAT_WEB_DIR" ]; then
            install_webchat
        fi
    fi

    # Save installation configuration
    save_install_config
}

# Uninstall function
uninstall() {
    echo ""
    echo "========================================"
    echo "  pyIRCX Uninstallation"
    echo "========================================"
    echo ""

    check_root

    echo -e "${YELLOW}Stopping service...${NC}"
    systemctl stop pyircx 2>/dev/null || true
    systemctl disable pyircx 2>/dev/null || true

    echo -e "${YELLOW}Removing files...${NC}"
    rm -f /etc/systemd/system/pyircx.service
    systemctl daemon-reload

    read -p "Remove installation directory $INSTALL_DIR? [y/N] " -n 1 -r
    echo
    if [[ $REPLY =~ ^[Yy]$ ]]; then
        rm -rf "$INSTALL_DIR"
    fi

    read -p "Remove configuration $CONFIG_DIR? [y/N] " -n 1 -r
    echo
    if [[ $REPLY =~ ^[Yy]$ ]]; then
        rm -rf "$CONFIG_DIR"
    fi

    read -p "Remove user $SERVICE_USER? [y/N] " -n 1 -r
    echo
    if [[ $REPLY =~ ^[Yy]$ ]]; then
        userdel "$SERVICE_USER" 2>/dev/null || true
    fi

    echo -e "${GREEN}Uninstallation complete${NC}"
}

# Parse arguments
case "${1:-}" in
    uninstall|remove)
        uninstall
        ;;
    *)
        main
        ;;
esac
